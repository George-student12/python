class Smartphone:
    Марка = "Неизвестно"
    Модель = "Неизвестно"
    Номер = "7-***-***-**-**"

    def __init__(self, маркателефона, модельтелефона, номеркарты):
        self.Марка = маркателефона
        self.Модель = модельтелефона
        self.Номер = номеркарты

    def список(self):
        return (f"Марка: {self.Марка},"
                f"Модель: {self.Модель}, "
                f"Номер: {self.Номер}")
